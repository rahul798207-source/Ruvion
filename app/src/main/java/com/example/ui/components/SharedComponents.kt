@Composable
fun AnalyticsCanvasChart(
    data: List<Float>,
    labels: List<String>,
    color: Color,
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier
            .fillMaxWidth()
            .background(MaterialTheme.colorScheme.surface, RoundedCornerShape(20.dp))
            .padding(16.dp)
    ) {
        Text(
            text = "Business Activity Matrix",
            style = MaterialTheme.typography.titleMedium.copy(fontWeight = FontWeight.Bold),
            modifier = Modifier.padding(bottom = 16.dp)
        )

        Canvas(
            modifier = Modifier
                .fillMaxWidth()
                .height(180.dp)
        ) {
            val width = size.width
            val height = size.height
            val paddingLeft = 30.dp.toPx()
            val paddingBottom = 20.dp.toPx()
            val usableWidth = width - paddingLeft
            val usableHeight = height - paddingBottom

            val maxVal = (data.maxOrNull() ?: 100f).coerceAtLeast(1f)

            // Draw grid lines
            val gridCount = 4
            for (i in 0..gridCount) {
                val y = usableHeight * (1 - i.toFloat() / gridCount)
                drawLine(
                    color = Color.LightGray.copy(alpha = 0.3f),
                    start = Offset(paddingLeft, y),
                    end = Offset(width, y),
                    strokeWidth = 1.dp.toPx()
                )
            }

            // Draw bars
            val barCount = data.size
            val barWidth = (usableWidth / barCount) * 0.6f
            val spacing = (usableWidth / barCount) * 0.4f

            for (i in 0 until barCount) {
                val rawVal = data[i]
                val barHeight = (rawVal / maxVal) * usableHeight
                val x = paddingLeft + i * (barWidth + spacing) + spacing / 2
                val y = usableHeight - barHeight

                drawRoundRect(
                    color = color,
                    topLeft = Offset(x, y),
                    size = Size(barWidth, barHeight),
                    cornerRadius = CornerRadius(4.dp.toPx(), 4.dp.toPx())
                )
            }
        }

        Spacer(modifier = Modifier.height(8.dp))

        // Draw labels
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(start = 30.dp),
            horizontalArrangement = Arrangement.SpaceBetween
        ) {
            for (label in labels) {
                Text(
                    text = label,
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.6f),
                    textAlign = TextAlign.Center,
                    modifier = Modifier.weight(1f)
                )
            }
        }
    }
}
