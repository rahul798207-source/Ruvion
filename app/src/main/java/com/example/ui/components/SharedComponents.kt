    val containerColor = when (role) {
        "Super Admin" -> MaterialTheme.colorScheme.tertiary.copy(alpha = 0.15f)
        "Business Owner" -> MaterialTheme.colorScheme.primary.copy(alpha = 0.15f)
        else -> SalesGreen.copy(alpha = 0.15f)
